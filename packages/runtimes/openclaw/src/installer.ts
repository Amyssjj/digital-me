// ─── Per-plugin descriptors ──────────────────────────────────────────────

export interface PluginInstallDescriptor {
  /** Plugin id (matches the manifest's id field, used as install dirname). */
  pluginDirname: string;
  /** Files copied verbatim into <extensionsDir>/<pluginDirname>/. */
  installFiles: ReadonlyArray<{ src: string; target: string }>;
  /** Human-readable name used in installer logs. */
  displayName: string;
}

