// ─── Generated package.json for the install target ───────────────────────

/**
 * Absolute on-disk roots of the workspace packages the plugin entries
 * import. Used to build the extension's package.json with `file:` deps
 * so Node's ESM loader can resolve `@digital-me/*` bare specifiers from
 * inside the openclaw extensions dir. openclaw's plugin loader does not
 * bundle deps, so a `node_modules` tree must sit alongside `index.mjs`.
 */
export interface ResolvedPackagePaths {
  brainOrchestrator: string;
  runtimeOpenclaw: string;
  contracts: string;
}

/**
 * Generate the package.json that goes into a plugin's install dir. Both
 * digital-me-brain and digital-me-recall depend on the same three
 * @digital-me/* packages, so we share the descriptor (different `name`
 * field per plugin keeps `npm install` outputs distinct).
 */
export function buildExtensionPackageJson(
  paths: ResolvedPackagePaths,
  pluginDirname: string = BRAIN_PLUGIN_DIRNAME,
): Record<string, unknown> {
  return {
    name: `${pluginDirname}-extension`,
    version: "0.0.0-local",
    private: true,
    type: "module",
    description: `Local install of ${pluginDirname} into openclaw's extensions directory. Auto-generated by \`digital-me install --runtime openclaw\`.`,
    // openclaw 2026.5+ requires this field on plugins under
    // `<extensionsDir>/`; without it the gateway's discovery skips
    // hooks-only plugins (tool-contract plugins like the brain happen
    // to be discovered via the manifest's contracts.tools field, which
    // hides the bug for tool-providing plugins).
    openclaw: { extensions: ["./index.mjs"] },
    dependencies: {
      "@digital-me/brain-orchestrator": `file:${paths.brainOrchestrator}`,
      "@digital-me/runtime-openclaw": `file:${paths.runtimeOpenclaw}`,
      "@digital-me/contracts": `file:${paths.contracts}`,
      yaml: "^2.6.0",
    },
  };
}

